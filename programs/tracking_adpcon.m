% containment control with respect to multiple leaders with bounded inputs.
% adaptive controller
% continuous controller, sigma-modification

function xdot=tracking_adpcon(t,x)

xdot=zeros(20,1);

% A=[0 1; -1 2]; B=[0; 1];
% A0=[0 1; -1 0];

K=[-1.6203   -4.7567];
G=[2.5255    7.7055; 7.7055    22.6266];
f1=0.005;

% the leader  #1
xdot(1) = x(2);
xdot(2) = -x(1);


% follower 1
xdot(3) = x(4);
xdot(4) = -x(3)+2*x(4)+x(15)*K*[x(3)-x(5)+x(3)-x(7)+x(3)-x(9)+x(3)-x(11)+x(3)-x(1); x(4)-x(6)+x(4)-x(8)+x(4)-x(10)+x(4)-x(12)+x(4)-x(2)]...
    +x(15)*adpunit(K*[x(3)-x(5)+x(3)-x(7)+x(3)-x(9)+x(3)-x(11)+x(3)-x(1); x(4)-x(6)+x(4)-x(8)+x(4)-x(10)+x(4)-x(12)+x(4)-x(2)],x(15));

% follower 2
xdot(5) = x(6);
xdot(6) = -x(5)+2*x(6)+x(16)*K*[x(5)-x(3)+x(5)-x(7)+x(5)-x(13); x(6)-x(4)+x(6)-x(8)+x(6)-x(14)]...
    +x(16)*adpunit(K*[x(5)-x(3)+x(5)-x(7)+x(5)-x(13); x(6)-x(4)+x(6)-x(8)+x(6)-x(14)],x(16));

% follower 3
